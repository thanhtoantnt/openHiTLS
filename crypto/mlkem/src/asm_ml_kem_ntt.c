/*
 * NTT/INTT twiddle table size (544 int16_t entries).
 *
 * Components (each stored as a Barrett pair, hence the final ×2):
 *   MLKEM_N_HALF - 1 = 127   twiddle factors for 7 NTT layers (2^0+...+2^6 roots)
 *   1                        alignment padding at table start
 *   16                       inter-section zero padding
 *   MLKEM_N_HALF = 128       duplicated entries for NEON vector layout
 */
#define MLKEM_NTT_TABLE_LEN (((MLKEM_N_HALF - 1) + 1 + 16 + MLKEM_N_HALF) * 2)

/*
 * Forward NTT twiddle factor table for Cooley-Tukey (CT) butterfly,
 * used by NEON assembly.
 *
 * ML-KEM NTT parameters: Q = 3329, omega = 17 (primitive 256th root of unity).
 * Each twiddle is stored as a Barrett pair (root, root_hi) where:
 *   root    = omega^k mod^{+-} Q          (signed canonical representative)
 *   root_hi = floor(round_even(root * 2^16 / Q) / 2)   (Barrett constant)
 * and k = bitreverse(2^layer + block, 7) for the negacyclic NTT.
 *
 * Generation script (verified against this table):
 *
 *   Q = 3329; omega = 17
 *
 *   def bitrev(x, n):
 *       r = 0
 *       for _ in range(n): r = (r << 1) | (x & 1); x >>= 1
 *       return r
 *
 *   def mod_pm(a):  # signed reduction mod Q
 *       r = a % Q
 *       return r - Q if r > Q // 2 else r
 *
 *   def barrett_hi(v):  # Barrett precomputed constant
 *       t = v * 2**16 / Q
 *       r = round(t)
 *       if r % 2 != 0: r = r + 1 if r <= t else r - 1  # round-to-even
 *       return r // 2
 *
 *   def twiddle(layer, block, inv=False):
 *       k = bitrev(2**layer + block, 7)
 *       if inv: k = -k
 *       root = mod_pm(pow(omega, k, Q))
 *       return (root, barrett_hi(root))
 *
 *   def emit_group(twiddles_4):  # 4 twiddle pairs -> 16 doubled entries
 *       grp = []
 *       for r, _ in twiddles_4: grp += [r, r]
 *       for _, h in twiddles_4: grp += [h, h]
 *       return grp
 *
 *   def gen_fwd_table():  # -> 544 entries
 *       T = [0, 0]  # alignment padding
 *       T += [v for l in range(4) for b in range(1<<l) for v in twiddle(l, b)]
 *       for sb in range(4):  # 4 super-blocks
 *           T += [0] * 16  # padding
 *           T += emit_group([twiddle(4,b) for b in [2*sb, 2*sb+8, 2*sb+1, 2*sb+9]])
 *           for g in range(2):  # layer 5
 *               T += emit_group([twiddle(5,b) for b in
 *                   [4*sb+g, 4*sb+16+g, 4*sb+2+g, 4*sb+18+g]])
 *           for g in range(4):  # layer 6
 *               T += emit_group([twiddle(6,b) for b in
 *                   [8*sb+g, 8*sb+32+g, 8*sb+4+g, 8*sb+36+g]])
 *       return T
 */
